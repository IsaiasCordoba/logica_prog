# Calcular área y perímetro de un círculo

# Entrada de datos

radio = float(input("Escribe el radio= "))
PI = 3.14165

# proceso

Area = PI * pow(radio, 2)
Perimetro = 2 * (PI * radio)

 # Salida de datos

print(f"El area del circulo es = {Area}")
print(f"El perimtro de el circulo es = {Perimetro}")
