# Determinar la edad de una persona.

# Entrada de datos

Año_de_nacimiento = int(input("Escribe el año de nacimiento =    "))
Año_actual = int(input("Escribe el año actual =    "))

# Proceso
Edad = Año_actual - Año_de_nacimiento

# Datos de salida
print(f"La edad del individuo es = {Edad}")